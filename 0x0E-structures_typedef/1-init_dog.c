#include "dog.h"
#include <stddef.h>
/**
 * init_dog -A function that initialize a variable
 * @d: pointer
 * @name: name of dog
 * @age: age of dog
 * @owner: name of owner
 */
void init_dog(struct dog *d, char *name, float age, char *owner)
{
	if (d != NULL)
	{
		d->name = name;
		d->age = age;
		d->owner = owner;
	}
}
